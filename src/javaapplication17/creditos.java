public class creditos {
    private int cantCredito;
    private int tipoCredito;
    private int numerocredito;
    private String estadoDeCredito;
    private int saldoCredito;
    private int FechaLimPago;
    private int fechalimvencimiento;

    public int getCantCredito() {
        return cantCredito;
    }

    public void setCantCredito(int cantCredito) {
        this.cantCredito = cantCredito;
    }

    public int getTipoCredito() {
        return tipoCredito;
    }

    public void setTipoCredito(int tipoCredito) {
        this.tipoCredito = tipoCredito;
    }

    public int getNumerocredito() {
        return numerocredito;
    }

    public void setNumerocredito(int numerocredito) {
        this.numerocredito = numerocredito;
    }

    public String getEstadoDeCredito() {
        return estadoDeCredito;
    }

    public void setEstadoDeCredito(String estadoDeCredito) {
        this.estadoDeCredito = estadoDeCredito;
    }

    public int getSaldoCredito() {
        return saldoCredito;
    }

    public void setSaldoCredito(int saldoCredito) {
        this.saldoCredito = saldoCredito;
    }

    public int getFechaLim() {
        return FechaLimPago;
    }

    public void setFechaLim(int FechaLim) {
        this.FechaLimPago = FechaLim;
    }

    public int getFechaLimVencimiento() {
        return fechalimvencimiento;
    }

    public void setFechaLimVencimiento(int fechaLimVencimiento) {
        this.fechalimvencimiento = fechaLimVencimiento;
    }

}
